# 2) კომენტარების სახით ახსენით თუ რა დანიშნულება აქვთ .append(); .insert() და .pop() ფუნქციებს.
# .append() - ამატებს ელემენტს სიის ბოლოში
my_list = [1, 2, 3]
my_list.append(4)  # შედეგი: [1, 2, 3, 4]

# .insert() - ამატებს ელემენტს სიის კონკრეტულ პოზიციაზე (ინდექსზე)
my_list.insert(1, 10)  # შედეგი: [1, 10, 2, 3, 4]  (10 ჩაემატა ინდექსზე 1)

# .pop() - შლის და აბრუნებს ელემენტს სიის კონკრეტული ინდექსიდან (სტანდარტულად ბოლო ელემენტს)
my_list.pop()        # შლის ბოლო ელემენტს: შედეგი იქნება [1, 10, 2, 3]
my_list.pop(1)       # შლის ინდექსზე 1 მდგომ ელემენტს: შედეგი იქნება [1, 2, 3]

# 3) შექმენით რამდენიმე ელენტისგან შემდგარი სია, თქვენი დავალებაა დაბეჭდოთ ამ სიის სიგრძე, ანუ სიაში არსებული ელემენტების რაოდენობა.
my_list = [10, 20, 30, 40, 50] 
print(len(my_list))  # len() აბრუნებს სიის სიგრძეს

# 4) შექმენით ხარიელი სია სადაც მომხმარებელს 5-ჯერ შემოატანინენებთ რიცხვს, შემდეგ კი დაამატებთ მას სიის ბოლოში append() ფუნქციით.
numbers = []  # ცარიელი სია

for i in range(5):
    num = int(input("შეიყვანე რიცხვი: ")) 
    numbers.append(num) 

print(numbers)  

# 5) მოცემულია სია:
# colors = ["red", "green", "blue", "yellow", "purple"]
# თქვენი დავალებაა სიიდან წაშალოთ ბოლო ელემენტი .pop() მეთოდის დახმარებით, შემდეგ კი დაბეჭდოთ განახლებული სია.
colors = ["red", "green", "blue", "yellow", "purple"]
colors.pop()  
print(colors)  

# 6) მოცემულია სია:
# animals = ["dog", "cat", "elephant", "lion"]
# თქვენი დავალებაა insert() მეთოდით ჩასვათ სიტყვა "monkey" სიაში მეორე პოზიციაზე, რის შემდეგაც დაბეჭდავთ განახლებულ სიას.
animals = ["dog", "cat", "elephant", "lion"]
animals.insert(1, "monkey") 
print(animals) 

# 7) შემქნით ცარიელი სია, სადაც 3-ჯერ input-ის სახით მომხმარებელს შეაყვანინებთ სამი სტუდენტის სახელს და დაამატებთ სიაში append() ფუნქციით. შემდეგ კი ჩასვით "Teacher" სიის თავში, წაშალეთ ბოლო სტუდენტი და დაბეჭდეთ სიის სიგრძე, ასვეე საბოლოო სია.
people = [] 

for i in range(3):
    name = input("შეიყვანე სტუდენტის სახელი: ")
    people.append(name)  

people.insert(0, "Teacher")  
people.pop()  

print("სიის სიგრძე:", len(people)) 
print("საბოლოო სია:", people)  

